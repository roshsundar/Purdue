void init_usart5() {
    RCC -> AHBENR |= RCC_AHBENR_GPIOCEN;
    RCC -> AHBENR |= RCC_AHBENR_GPIODEN;
    GPIOC -> MODER &= ~(GPIO_MODER_MODER12);
    GPIOD -> MODER &= ~(GPIO_MODER_MODER2);
    GPIOC -> MODER |= GPIO_MODER_MODER12_1;
    GPIOD -> MODER |= GPIO_MODER_MODER2_1;
    GPIOC -> AFR[1] |= 0x00020000; // port c pin 12 use AF2
    GPIOD -> AFR[0] |= 0x00000200; // port d pin 2 use AF2

    RCC -> APB1ENR |= RCC_APB1ENR_USART5EN;
    USART5 -> CR1 &= ~USART_CR1_UE;
    USART5 -> CR1 &= ~(0x00001000 | 0x01000000);
    USART5 -> CR2 &= ~USART_CR2_STOP;
    USART5 -> CR1 &= ~USART_CR1_PCE;
    USART5 -> CR1 &= ~USART_CR1_OVER8;
    USART5 -> BRR = 48000000 / 115200;
    USART5 -> CR1 |= USART_CR1_TE | USART_CR1_RE;
    USART5 -> CR1 |= USART_CR1_UE;
    while ((USART5 -> ISR & USART_ISR_TEACK) == 0);
    while ((USART5 -> ISR & USART_ISR_REACK) == 0);
}

